# Функция для расчета площади квадрата
def area(a):
    '''
    Возвращает площадь квадрата по формуле a^2
    Пример вызова:
    print(area(2))
    -> 4
    '''
    return a * a

# Функция для расчета периметра квадрата
def perimeter(a):
    '''
    Возвращает периметр квадрата по формуле 4a
    Пример вызова:
    print(perimeter(5))
    -> 20
    '''
    return 4 * a
